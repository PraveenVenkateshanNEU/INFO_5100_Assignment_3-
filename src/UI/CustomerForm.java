    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        String firstName = firstNameTextField.getText(); 
        String lastName = lastNameTextField.getText();
        String age = ageTextField.getText();
        String email = emailTextField.getText();
        String message = messageTextField.getText(); 
        if(firstName.isEmpty()||lastName.isEmpty()||age.isEmpty()||email.isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing data in one or more mandatory fields");
        }
        else{
            if(val==1){
                JOptionPane.showMessageDialog(this, "Enter valid data in mandatory fields");
            }
            else if(im==1){
                 JOptionPane.showMessageDialog(this, imageLabel.getIcon().toString().substring(0,1).replace("j", "")+firstName+" "+lastName+"\n"+age+"\n"+email+"\n"+message,"CUSTOMER INFORMATION", HEIGHT, imageLabel.getIcon());
            }
            else{
                JOptionPane.showMessageDialog(this, firstName+" "+lastName+"\n"+age+"\n"+email+"\n"+message,"Customer Information",HEIGHT);
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

    private void firstNameTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_firstNameTextFieldKeyReleased
        // TODO add your handling code here:
        String pattern = "^[a-zA-Z]{0,15}$";
        Pattern pat = Pattern.compile(pattern);
        Matcher match = pat.matcher(firstNameTextField.getText());
        if(!match.matches()){
            fLabel.setText("Enter only text data");
            val=1;
        }
        else{
            fLabel.setText(null);
            if(val>0){
                val=0;
            }
        }
    }//GEN-LAST:event_firstNameTextFieldKeyReleased

